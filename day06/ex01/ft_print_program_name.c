void	ft_putchar(char c);

void	ft_putstring(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int		main(int argc, char **argv)
{
	ft_putstring(argv[0]);
	ft_putchar('\n');
	return (0);
}
