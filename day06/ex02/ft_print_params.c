void	ft_putchar(char c);

void	ft_putstring(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int		main(int argc, char **argv)
{
	int		i;

	i = 1;
	while (argv[i])
	{
		ft_putstring(argv[i]);
		ft_putchar('\n');
		i++;
	}
	ft_putchar('\n');
	return (0);
}
